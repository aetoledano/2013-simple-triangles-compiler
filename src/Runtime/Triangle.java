
package Runtime;

public class Triangle {
    
    private int dim,x,y;

    public Triangle(int dim, int x, int y) {
        this.dim = dim;
        this.x = x;
        this.y = y;
    }

    public int getDim() {
        return dim;
    }

    public void setDim(int dim) {
        this.dim = dim;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
}
